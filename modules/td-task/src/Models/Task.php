<?php

// -------------------------------------------------
// Namespace
// -------------------------------------------------
namespace tronderdata\TdTask\Models;

// -------------------------------------------------
// Dependencies
// -------------------------------------------------
use Illuminate\Database\Eloquent\Model;
use App\Models\User;

class Task extends Model
{
    // -------------------------------------------------
    // Fillable
    // -------------------------------------------------
    protected $fillable = ['title', 'description', 'due_date', 'created_by', 'child_task_id', 'status_id', 'assigned_to', 'wall_id', 'category_id', 'estimated_time', 'actual_time'];



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION CATEGORY
    // Relationships between category and tasks, if category exists
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function category()
    {
        // --------------------------------------------------------------------------------------------------
        // If any of this category exists, return the relationship between category and tasks
        // --------------------------------------------------------------------------------------------------

        // -------------------------------------------------
        // TD-Categories
        // -------------------------------------------------
        if (class_exists(\tronderdata\categories\Models\Category::class)) {
            
            // Return
            return $this->belongsTo(\tronderdata\categories\Models\Category::class, 'category_id');
        }

        // -------------------------------------------------
        // Return null if category does not exist
        // -------------------------------------------------
        return null;
    }



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION CREATOR
    // Relationships between creators and tasks
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function creator()
    {
        // -------------------------------------------------
        // Return the relationship between tasks and users
        // -------------------------------------------------
        return $this->belongsTo(User::class, 'created_by');
    }



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION ChildTask
    // Relationships between child tasks and tasks
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function childTasks()
    {
        return $this->hasMany(Task::class, 'child_task_id');
    }



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION ParentTask
    // Relationships between parent tasks and child tasks
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function parentTask()
    {
        return $this->belongsTo(Task::class, 'child_task_id');
    }



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION ParentTask
    // Relationships to task statuses for tasks
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function status()
    {
        // -------------------------------------------------
        // Return the relationship between tasks and taskstatuses
        // -------------------------------------------------
        return $this->belongsTo(TaskStatus::class, 'status_id');
    }



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION ASSIGNEE
    // Relationships between assignees and tasks
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function assignee()
    {
        // -------------------------------------------------
        // Return the relationship between tasks and the assigned user
        // -------------------------------------------------
        return $this->belongsTo(User::class, 'assigned_to');
    }


    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION COMMENTS
    // Relationships between comments and tasks
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function comments()
    {
        return $this->hasMany(TaskComment::class);
    }


    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION WALL
    // Relationships between task walls and tasks
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function wall()
    {
        return $this->belongsTo(TaskWall::class, 'wall_id');
    }

}
