<?php
// -------------------------------------------------
// Namespace
// -------------------------------------------------
namespace tronderdata\TdTasks\Models;

// -------------------------------------------------
// Dependencies
// -------------------------------------------------
use Illuminate\Database\Eloquent\Model;

class Task extends Model
{
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION __construct
    //
    // Eloquent-modell for oppgaver
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    protected $fillable = [

        // -------------------------------------------------
        // Felter som kan fylles ut
        // -------------------------------------------------
        'task_wall_id', 'title', 'description', 'assigned_to', 'due_date', 'status_id'
    ];



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION wall
    // Relasjon til veggen oppgaven tilhÃ¸rer
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function wall()
    {

        // -------------------------------------------------
        // Returner relasjonen
        // -------------------------------------------------
        return $this->belongsTo(TaskWall::class);
    }



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION status
    // Relasjon til statusen oppgaven har
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function status()
    {
        // -------------------------------------------------
        // Returner relasjonen
        // -------------------------------------------------
        return $this->belongsTo(TaskStatus::class);
    }



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION assignee
    // Relasjon til brukeren som er tildelt oppgaven
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function assignee()
    {
        // -------------------------------------------------
        // Returner relasjonen
        // -------------------------------------------------
        return $this->belongsTo(User::class, 'assigned_to');
    }



    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    // FUNCTION dependencies
    // Relasjon til avhengigheter oppgaven har
    //
    // ---------------------------------------------------------------------------------------------------------------------------------------------------
    public function dependencies()
    {
        // -------------------------------------------------
        // Returner relasjonen
        // -------------------------------------------------
        return $this->hasMany(TaskDependency::class, 'task_id');
    }
}
